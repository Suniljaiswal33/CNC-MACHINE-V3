with BuildSketch(Plane.XY.offset(17.151)) as second_cut_sketch:
    with BuildLine():
        Polyline(*second_profile_points, close=True)
    make_face()

extrude(
    second_cut_sketch.face(),
    amount=-100,
    mode=Mode.SUBTRACT
)

# =====================================================
# NEW THRU CUT - close boundary + through all
# =====================================================

with BuildPart() as thru_tool:
    with BuildSketch(Plane.XY.offset(17.151)):
        with BuildLine():
            Polyline(*thru_cut_points, close=True)
        make_face()
    extrude(amount=500, both=True)

Y_mount.part = Y_mount.part.cut(thru_tool.part)

# =====================================================
# EXPORT STL
# =====================================================

desktop = Path.home() / "Desktop"
stl_path = desktop / f"{part_name}.stl"
export_stl(Y_mount.part, str(stl_path))
print("STL Exported:", stl_path)
